package handler

import (
	"net/http"
)

// NewHealthzHandler returns an http.Handler that always responds 200 ok (liveness probe).
func NewHealthzHandler() http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write([]byte("ok"))
	})
}
